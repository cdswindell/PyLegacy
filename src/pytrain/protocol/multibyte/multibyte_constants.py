class MultiByteCommandDef(TMCC2CommandDef):
    __metaclass__ = abc.ABCMeta

    def __init__(
        self,
        prefix: TMCC2ParameterIndex,
        command_bits: int,
        d_min: int = 0,
        d_max: int = 0,
        interval: int = None,
        noop: bool = False,
    ) -> None:
        super().__init__(command_bits, d_min=d_min, d_max=d_max, interval=interval, noop=noop)
        self._first_byte = TMCC2CommandPrefix.ENGINE
        self._second_byte = prefix


